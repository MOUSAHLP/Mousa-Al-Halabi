let imgInterval;
let imgIntervalTimer = 2000;
let youtubeBtn = document.querySelector(".modal-footer .btn.Youtube");
let githubBtn = document.querySelector(".modal-footer .btn.Github");
let websiteeBtn = document.querySelector(".modal-footer .btn.Website");

let modal = document.querySelector(".modal.fade-scale");
function showAppPage(appName) {
  let img = document.querySelector(".modal.fade-scale img");
  let title = document.querySelector(
    ".modal.fade-scale .modal-body .modal-title"
  );
  let disc = document.querySelector(".modal.fade-scale .modal-body p");

  let imgCount = 0;
  img.src = apps[appName]["images"][imgCount];
  imgInterval = setInterval(() => {
    img.src = apps[appName]["images"][imgCount];

    if (imgCount == apps[appName]["images"].length - 1) {
      imgCount = 0;
    } else {
      imgCount++;
    }
  }, imgIntervalTimer);

  title.innerHTML = apps[appName]["name"];

  let currLang = localStorage.getItem("currLang")??"en";
  
  if (currLang != "en"){
    disc.innerHTML = apps[appName]["disc_de"];
  }
  else{
    disc.innerHTML = apps[appName]["disc"];
  }


  if (apps[appName]["youtube"] != null) {
    youtubeBtn.href = apps[appName]["youtube"];
    youtubeBtn.classList.add("show");
  }

  if (apps[appName]["github"] != null) {
    githubBtn.href = apps[appName]["github"];
    githubBtn.classList.add("show");
  }

  if (apps[appName]["view-webSite"] != null) {
    websiteeBtn.href = apps[appName]["view-webSite"];
    websiteeBtn.classList.add("show");
  }

  let backgroundDiv = document.createElement("div");
  backgroundDiv.classList.add("modal-backdrop");
  backgroundDiv.classList.add("fade");
  backgroundDiv.classList.add("in");
  document.body.appendChild(backgroundDiv);

  modal.classList.add("in");

  backgroundDiv.onclick = () => {

    window.clearInterval(imgInterval);
    youtubeBtn.classList.remove("show");
    githubBtn.classList.remove("show");
    websiteeBtn.classList.remove("show");

    backgroundDiv.remove();
    modal.classList.remove("in");
  };
}
let closeDialog = document.querySelector(".modal.fade-scale a.rectangle");
closeDialog.onclick = () => {
  let backgroundDiv = document.querySelector(".modal-backdrop");

  window.clearInterval(imgInterval);

  youtubeBtn.classList.remove("show");
  githubBtn.classList.remove("show");
  websiteeBtn.classList.remove("show");
  backgroundDiv.remove();
  modal.classList.remove("in");
};
